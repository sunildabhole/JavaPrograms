package basicsprograms;

public class MathClass {

	public static void main(String[] args) {
		System.out.println(Math.addExact(100, 200));
		System.out.println(Math.addExact(453545l, 5555521));
		System.out.println(Math.subtractExact(423, 23));
		System.out.println(Math.subtractExact(532352l, 312323));
		System.out.println(Math.multiplyExact(10, 10));
		System.out.println(Math.multiplyExact(24552l, 10));
		System.out.println(Math.multiplyExact(5352252l, 425525));
		System.out.println(Math.max(3.13324, 23.43241));
		System.out.println(Math.max(2.1, 1.2));
		System.out.println(Math.max(34, 23));
		System.out.println(Math.max(64643534l, 4753242));
		System.out.println(Math.min(3.13324, 23.43241));
		System.out.println(Math.min(2.1, 1.2));
		System.out.println(Math.min(34, 23));
		System.out.println(Math.min(64643534l, 4753242));
		System.out.println(Math.abs(-2.33424));
		System.out.println(Math.abs(-34));
		System.out.println(Math.abs(-646));
		System.out.println(Math.random());
		System.out.println(Math.PI);
	}

}
