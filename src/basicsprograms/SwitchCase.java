package basicsprograms;

public class SwitchCase {

	public static void main(String[] args) {
		switch (2) {
		case 1:
			System.out.println("Print 1 case");
			break;
		case 2:
			System.out.println("Print 2 case");
			break;
		case 3:
			System.out.println("Print 3 case");
			break;
		case 4:
			System.out.println("Print 4 case");
			break;
		default:
			System.out.println("Wrong no case");
		}
	}
}
